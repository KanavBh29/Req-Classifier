  const exportTraceCsv = async () => {
    setLoad('trace-csv', true);
    try {
      const res = await reportAPI.traceability();
      const links = res.data.data.links || [];
      downloadCSV(links.map(l => ({
        RequirementID: l.requirementId?.requirementId,
        Requirement: (l.requirementId?.text || '').slice(0, 80),
        TestCaseID: l.testCaseId?.testCaseId,
        TestCase: l.testCaseId?.description,
        Status: l.validationStatus,
      })), ['RequirementID', 'Requirement', 'TestCaseID', 'TestCase', 'Status'], 'traceability-' + Date.now() + '.csv');
      toast.success('Traceability CSV downloaded!');
    } catch (err) {
      console.error('Trace CSV error:', err);
      toast.error('Export failed');
    } finally { setLoad('trace-csv', false); }
  };

  // ---- KPI ----
